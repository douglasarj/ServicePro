    private void btAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAdicionarMouseClicked
        try {
            Connection con = Conexao.conexaoBanco();
            String sql = "INSERT INTO manutencao(equipamento,tipo,defeito,servico,tecnico,valor,situacao,id_cliente) VALUES(?,?,?,?,?,?,?,?)";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, txtEquipamento.getText());
            stmt.setString(2, tipo);
            stmt.setString(3, txtDefeito.getText());
            stmt.setString(4, txtServico.getText());
            stmt.setString(5, txtTecnico.getText());
            stmt.setString(6, valorTotal.getText().replace(",", "."));
            stmt.setString(7, situacao.getSelectedItem().toString());
            stmt.setString(8, txtID.getText());

            if ((txtID.getText().isEmpty()) || (txtEquipamento.getText().isEmpty()) || (txtDefeito.getText().isEmpty()) || situacao.getSelectedItem().equals(" ")) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios");
            } else {
                JOptionPane.showMessageDialog(null, "OS emitida com sucesso");
                btAdicionar.setEnabled(false);
                btPesquisar.setEnabled(false);
               
            }

            stmt.execute();
            stmt.close();
            con.close();

            txtEquipamento.setText(null);
            txtDefeito.setText(null);
            txtServico.setText(null);
            txtTecnico.setText(null);
            valorTotal.setText(null);
            txtID.setText(null);
        } catch (SQLException ex) {
            Logger.getLogger(TelaServicos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAdicionarMouseClicked

