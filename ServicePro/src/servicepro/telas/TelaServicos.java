    private void txtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyReleased
        try {
            Connection con = Conexao.conexaoBanco();
            String sql = "SELECT id_cliente, nome_cliente, telefone_cliente FROM cliente WHERE nome_cliente LIKE '%" + txtPesquisa.getText() + "%' ORDER BY id_cliente";
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            DefaultTableModel modeloTabela = (DefaultTableModel) tabelaCliente.getModel();
            modeloTabela.setNumRows(0);
            while (rs.next()) {
                Object[] dados = {
                    rs.getString("id_cliente"),
                    rs.getString("nome_cliente"),
                    rs.getString("telefone_cliente")};
                modeloTabela.addRow(dados);
            }
            rs.close();
            stmt.close();
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtPesquisaKeyReleased

